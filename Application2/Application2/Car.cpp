#include "Car.h"
void Car::setMaker(string m)
{
	maker = m;
}

string Car::getMaker()
{
	return maker;
}

void Car::setModel(int n)
{
	model = n;
}

int Car::getModel()
{
	return model;
}

void Car::setColor(string c)
{
	color = c;
}

string Car::getColor()
{
	return color;
}


